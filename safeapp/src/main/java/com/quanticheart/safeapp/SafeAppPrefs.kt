package com.quanticheart.safeapp

import android.content.Context

private const val prefsName = "ProtectedApps"
private const val prefsStatus = "skipProtectedAppCheck"

fun Context.showDialog(): Boolean {
    val settings = getSharedPreferences(prefsName, Context.MODE_PRIVATE)
    return settings.getBoolean(prefsStatus, true)
}

fun Context.notShowDialogAgain() {
    val settings = getSharedPreferences(prefsName, Context.MODE_PRIVATE)
    val editor = settings.edit()
    editor.putBoolean(prefsStatus, false)
    editor.apply()
}
